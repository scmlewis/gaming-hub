export function getTileColor(value: number, theme: string): string {
  const selectedTheme = TILE_THEMES[theme] || TILE_THEMES.classic
  return selectedTheme.colors[value] || selectedTheme.colors[8192]
}

export function getTileTextColor(value: number, theme: string): string {
  const selectedTheme = TILE_THEMES[theme] || TILE_THEMES.classic
  return selectedTheme.textColors[value] || selectedTheme.textColors[8192]
}
